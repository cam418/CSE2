/*
Write a program that prompts the user to enter a double and then prints out a 
crude estimate of the cube root of the number and the value of this crude guess
when cubed. 

If x is the number entered. Then start by guessing the square root is x/3. Then
improve this guess by replacing guess with (guess*guess*guess+x)/(3*guess*guess).
Improve the guess four more times by coding the substitution. 

For example, to find a crude guess for the cube root of 27, guess 27/3=9. 
Then replace guess with 
(2*guess*guess*guess+x)/(3*guess*guess) = (2*9*9*9+27)/(3*9*9) = (2*729+27)/(243) = 6. 
*/

import java.util.Scanner;

public class Root{
    
    public static void main(String [] args){
        
        Scanner in = new Scanner(System.in);  //input
       
        System.out.print("Enter a double and I print its cube root-"); //prompting
        
        double x = in.nextDouble();
        double guess = x/3; 
        guess = (guess*guess*guess+x)/(3*guess*guess) ;
        guess = (guess*guess*guess+x)/(3*guess*guess) ;
        guess = (guess*guess*guess+x)/(3*guess*guess) ;
        guess = (guess*guess*guess+x)/(3*guess*guess) ;
        guess = (guess*guess*guess+x)/(3*guess*guess) ;
        
        guess = (2*guess*guess*guess+x)/(3*guess*guess);
        
        System.out.println("The cube root is " + guess) ;
    }
}